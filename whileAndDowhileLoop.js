//in this we will see the uses of While and Do while loop
/*
while (condition) {
    
}
*/


//example while loop

let score = 0;
while (score<=10) {
    console.log(`score is ${score}`)
    score = score + 1;
}

/*Do-while
do {
    
} while (condition);
*/

//example do-while

let points  = 5;
do {
    console.log(`points are ${points}`)
    points++;
} while (points<=10);